console.log("Print table of 10");
let n = 1;
while (n <= 100) {
  if (n % 10 == 0) {
    console.log(n);
  }
  n++;
}

console.log("Print reverse table of 10");
let m = 100;
while (m >= 10) {
  if (m % 10 == 0) {
    console.log(m);
  }
  m--;
}
